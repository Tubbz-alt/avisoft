    private void reset() {
        txtEmail.requestFocus();
        cargar();
        txtClave.setText(null);
        txtRepClave.setText(null);
    }
    
    private void cargar() {
        txtEmail.setText(log.getEmail());
        txtEmail.selectAll();
    }
    
