    private void cmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGuardarActionPerformed
        // TODO add your handling code here:
        String res = log.set(txtEmail.getText(), txtClave.getPassword(), txtRepClave.getPassword());
        if(res.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Configuraci√≥n guardada", "Configurar cuenta", javax.swing.JOptionPane.PLAIN_MESSAGE, new javax.swing.ImageIcon(getClass().getResource("/Images/save.png")));
            this.reset();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, res, "Error!!", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cmdGuardarActionPerformed

